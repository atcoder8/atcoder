fn solve() -> Mint {
    input! {
        n: usize,
        aa: [Usize1; n],
    }

    let mut used = vec![false; n];
    let mut parents: Vec<usize> = (0..n).collect_vec();
    for (i, &a) in enumerate(&aa).rev() {
        if a < i {
            return Mint::new(0);
        }

        if a > i {
            used[parents[a]] = true;
            parents[a] = i;
        }
    }

    let mut ans = Mint::new(1);
    let mut sum = 0;
    for (i, &a) in enumerate(&aa) {
        sum += !used[i] as usize;

        if i == a {
            if sum == 0 {
                return Mint::new(0);
            }

            ans *= sum;
            sum -= 1;
        }
    }

    ans
}

